// Write a java program to swap two integer numbers. 

class pro3
{

    public static void main(String[] args)
    {
        int  a = 10 , b = 20 , c;

        c = a;
        a = b;
        b = c; 

        System.out.println("Value of a : " + a +
                           "\nvalue of b : " + b);
    }

}