// Write a java program that declares integer variable and float variable.
// Initialize them, add and multiply both variables and print the output.

class pro2
{
    public static void main(String[] args)
    {

        int i = 20;
        float f = 10.20f;

        System.out.println("Addition of two variables is " + (i + f));
        System.out.println("Multiplication of two variables is " + (i * f));
    }
    
}