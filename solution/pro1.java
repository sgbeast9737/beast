// Write a java program to create variable of type char, short, int, float and
// double. Each should be initialized and their values are displayed.

class pro1
{
    public static void main(String[] args)
    {
        char c = 'S';
        short s = 10;
        int i = 20;
        float f = 30.20f;
        double d = 50.90;

        System.out.println("char value :" + c + 
                           "\nshort value :" + s +
                           "\nint value :" + i +
                           "\nfloat value :" + f +
                           "\ndouble value :" + d);
    }
}